# Data Type = Jenis Jenis Data pada Variabel

'''
Data Type ( General ) 

1. int = integer(numberic) = angka bilangan bulat , contoh 10 , 5, 13

2. float = float(numberic) = angka bilangan desimal ( berkoma ) , contoh 3.14 , 15.5 

3. str = string = kumpulan karakter karakter tesk dengan di apit  simbol karakter singletick ( '' ) atau doubletick ( "" ) , contoh "Ora Mangan Ora Urip" , 'Zaky Raihan'

4. bool = boolean(numberic) = nilai biner true(1)/false(0) , contoh dpr_korupsi = True

'''

# integer
kelas = 10
absen = 5 
print("Halo saya kelas", kelas, "dengan absen", absen, type(kelas))

# float
PHI = 3.14
print(PHI * 2, type(PHI))

# String
nama_saya = "Alwanu Zaky Raihan"
print("Halo Nama saya", nama_saya, type(nama_saya))

# boolean
status_pelajar = True # huruf depan T harus besar/Kapital = True
ada_ktp = False # huruf depan F harus besar/Kapital = False

print("Saya seorang pelajar", status_pelajar, type(status_pelajar))

